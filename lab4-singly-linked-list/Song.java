public class Song {
    private String title;
    private String artist;
    // Constructor 
    public Song(String title, String artist){
        this.title = title;
        this.artist = artist;
    }
    
    //getters
    public String getTitle()
    {
        return title;
    }
    
    public String getArtist()
    {
        return artist;
    }
    
    @Override
    public String toString()
    {
        return "Title: " + this.getTitle() + ", Artist: " + this.getArtist();
    }
}
